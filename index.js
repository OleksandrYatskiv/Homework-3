firstNumber = parseFloat(prompt("Enter a number:"));
secondNumber = parseFloat(prompt("Enter another number:"));

sumResult = firstNumber + secondNumber;
minusResult = firstNumber - secondNumber;
multipleResult = firstNumber * secondNumber;
devideResult = firstNumber / secondNumber;

alert(`${firstNumber} + ${secondNumber} = ${sumResult} 
${firstNumber} - ${secondNumber} = ${minusResult}
${firstNumber} * ${secondNumber} = ${multipleResult}
${firstNumber} / ${secondNumber} = ${devideResult}`);